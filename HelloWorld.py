# INTRODUCTION #

# simple, basic syntax.
print("Hello World!")

# longer syntax, but more specific. let's everyone know what exactly you are printing.
# declare a variable: hello_string is the variable name
hello_string = "Hello World!";
# print that variable
print(hello_string);
# allows you to change the value of that variable whenever
hello_string = "new world";
print(hello_string);


